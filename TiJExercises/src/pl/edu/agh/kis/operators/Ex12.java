package pl.edu.agh.kis.operators;

/**
 * Wynik przesuwania liczby binarnie w lewo, jak widać najmłodszy bit jest 
 * zastępowany przez 0, pozostałe zostają przesunięte
 * @author Szymon
 * @version 1.6.3
 *
 */
public class Ex12 {
	
	/** Punkt wyjścia do klasy i aplikacji
	 *  @param args tablica ciągów argumentów wywołania
	 */	
	public static void main(String[] args)
	{	
		int	number = 0xffffffff;
		
		number = number << 1;
		
		for(int i = 0; i<33; i++)
		{
			System.out.println(Integer.toBinaryString(number));
			number = number >>> 1;
		}
	}
}
